"""
Écrire une fonction produit prenant en paramètres d'entrée deux nombres entiers et retournant leur produit.
"""

def Produit(pEntier1, pEntier2):
    return pEntier1 * pEntier2

# Appel de la fonction
premier_nombre = 3
deuxieme_nombre = 6
print("Le produit " + str(premier_nombre) + "x" + str(deuxieme_nombre) + "=" + str(Produit(premier_nombre, deuxieme_nombre)))
